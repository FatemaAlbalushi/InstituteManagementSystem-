
/**
 * Staff class sshows  a staff with staffId, name, phone , email,
 * address fields.
 */
public class Staff {
	
		/**
	 * @param staffId
	 * @param name
	 * @param phone
	 * @param email
	 * @param address
	 */
	public Staff(int staffId, String name, String phone, String email, String address) {
		super();
		this.staffId = staffId;
		this.name = name;
		this.phone = phone;
		this.email = email;
		this.address = address;
	}
		 int staffId;
		 String name;
		 String phone;
		 String email;
		 String address;
		
	

}
